
-- Stored Procedure

------------------------------------------------------------------------------------------------------------

-- Procedimentos armazenados são uma coleção de comandos SQL compilados e armazenados no Banco de Dados.

	-- Separaração dos códigos SQL da programação;
	-- Melhoram o tráfego;
	-- Repositório unificado dos códigos SQL;
	-- Melhoram a manutenção e das aplicações no Banco de Dados.

-- Regras de criação

	-- Somente usuários admin (SA);
	-- Referenciar tabelas, views, outros stored procedures e tabelas temporárias;
	-- Comandos que não se pode utilizar: CREATE PROCEDURE, CREATE DEFAULT, CREATE RULE, CREATE TRIGGER E CREATE VIEW.

